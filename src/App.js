function MovieDetails({ selectedId, onCloseMovie, onAddWatched, watched }) {
  // What we pass to useState is initial state
  // React only looks at it at initial render
  const [movie, setMovie] = useState({});

  // Updating state is asynchronous
  const [isLoading, setIsLoading] = useState(false);
  const [userRating, setUserRating] = useState('');

  const isWatched = watched.map(mov => mov.imdbID).includes(selectedId);
  const watchedUserRating = watched.find(
    mov => mov.imdbID === selectedId
  )?.userRating;

  const {
    Title: title,
    Year: year,
    Poster: poster,
    Runtime: runtime,
    imdbRating,
    Plot: plot,
    Released: released,
    Actors: actors,
    Director: director,
    Genre: genre,
  } = movie;

  // derived state
  // gets created on each re-render
  const isTop = imdbRating > 8;
  console.log(isTop);

  useEffect(
    function () {
      // Function for event
      function callback(e) {
        if (e.code === 'Escape') {
          onCloseMovie();
        }
      }
      // Add event listener
      document.addEventListener('keydown', callback);
      // Cleanup event listener
      return function () {
        // Function must be same as one above
        document.removeEventListener('keydown', callback);
      };
    },
    [onCloseMovie]
  );

  function handleAdd() {
    const newWatchedMovie = {
      imdbID: selectedId,
      title,
      year,
      poster,
      imdbRating: Number(imdbRating),
      runtime: Number(runtime.split(' ').at(0)),
      userRating,
    };

    onAddWatched(newWatchedMovie);
    onCloseMovie();
  }

  useEffect(
    function () {
      async function getMovieDetails() {
        setIsLoading(true);
        const res = await fetch(`${API}&i=${selectedId}`);

        const data = await res.json();
        setMovie(data);
        setIsLoading(false);
      }
      getMovieDetails();
    },
    [selectedId]
  );

  // Each effect has only one purpose
  useEffect(
    function () {
      if (!title) return;
      document.title = `Movie | ${title}`;
      // Cleanup function
      // Executes when component unmounts
      return function () {
        document.title = 'usePopcorn';
      };
    },

    [title]
  );

  return (
    <div className="details">
      {isLoading ? (
        <Loader />
      ) : (
        <>
          <header>
            <button className="btn-back" onClick={onCloseMovie}>
              &larr;
            </button>
            <img src={poster} alt={`Poster of ${movie} movie`} />
            <div className="details-overveiw">
              <h2>{title}</h2>
              <p>
                {released} &bull; {runtime}
              </p>
              <p>{genre}</p>
              <p>
                <span>‚≠ê</span>
                {imdbRating} IMDb rating
              </p>
            </div>
          </header>

          <section>
            <div className="rating">
              {!isWatched ? (
                <>
                  <StarRating
                    maxRating={10}
                    size={24}
                    onSetRating={setUserRating}
                  />
                  {userRating > 0 && (
                    <button className="btn-add" onClick={handleAdd}>
                      + Add to list
                    </button>
                  )}
                </>
              ) : (
                <p>
                  You rated this movie with {watchedUserRating} <span>‚≠ê</span>
                </p>
              )}
            </div>
            <p>
              <em>{plot}</em>
            </p>
            <p>Starring {actors}</p>
            <p>Directed by {director}</p>
          </section>
        </>
      )}
    </div>
  );
}

